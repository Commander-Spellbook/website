// because I'm lazy, this was pulled from
// https://gist.github.com/c0derabbit/9ad0c77f2713de58fa1c4c0e74199d33
// I know this isn't the most secure way to generate randomness,
// but since we're not worried about uniaueness, it's just a fun easter
// egg for someone who logs in without first creating an account, we
// don't need to worry about it
function randomChoice(arr: string[]): string {
  return arr[Math.floor(Math.random() * arr.length)];
}

